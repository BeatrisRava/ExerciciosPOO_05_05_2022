public class Ex01Impressora {

    public void exibir(float num1){
        System.out.println(num1);
    }

    public void exibir(float num1, float num2){
       System.out.println(num1 + ", "+ num2);
    }

    public void exibir(float num1, String string1){
        System.out.println(num1 + ", " + string1);
    }

    public void exibir(String string1, float num1){
        System.out.println(string1 + ", " + num1);
    }

    public void exibir(String string1, String string2, String string3){
        System.out.println(string1 + ", " + string2 + ", " + string3);
    }

    public void exibir(int num1, int num2, String string1){
        System.out.println(num1 + ", " + num2 + ", " + string1);
    }
}